class Caluladora {
    sumar(num1, num2) {
      let resultado = num1 + num2;
      return resultado;
    }
    restar(num1, num2) {
      let resultado = num1 - num2;
      return resultado;
    }
    dividir(num1, num2) {
      let resultado = num1 / num2;
      return resultado;
    }
    multipicar(num1, num2) {
      let resultado = num1 * num2;
      return resultado;
    }
  }